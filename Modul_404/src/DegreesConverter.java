public class DegreesConverter {

  public static double toFahrenheit(double celsius) { // was muss hier hinzugefügt werden?
    return (celsius * 9.0/5.0) + 32.0;
  }

  public static double toCelsius(double fahrenheit) { // was muss hier hinzugefügt werden?
    return (fahrenheit - 32.0) * 5.0/9.0;
  }

}